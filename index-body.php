		<h3>About Hamlets</h3>
		<p>
			A hamlet is a small community located within a town. In order for a community 
			to be recognized as a hamlet, it must have basic public facilities for education, 
			administration and social gatherings (such as schools, churches and post offices). 
			Privately owned businesses and trade buildings also make up a hamlet. These include, 
			but are not limited to: blacksmith shops, gristmills, wagon maker's shops, general stores, 
			hotels and produce markets. Buildings that connect the community to important transportation 
			routes, train stations and trolley stops, are also vital to recognizing an area as a hamlet.
		</p>
